pub struct FramebufferConsole {
    addr: usize,
    pitch: u32,
    pub width: u32,
    pub height: u32,
    bpp: u8,
}

impl FramebufferConsole {
    pub fn probe(mboot_magic: u32, info_addr: u32) -> Option<Self> {
        if multiboot2::is_multiboot2(mboot_magic) {
            if let Some(fb) = multiboot2::framebuffer_info(info_addr) {
                return Self::from_raw(fb.addr as usize, fb.pitch, fb.width, fb.height, fb.bpp);
            }
        }
        if let Some(fb) = Self::from_multiboot(info_addr) {
            return Some(fb);
        }
        if let Some(gop) = efi_gop::from_multiboot(info_addr) {
            if gop.addr <= u32::MAX as u64 {
                return Self::from_raw(
                    gop.addr as usize,
                    gop.pitch,
                    gop.width,
                    gop.height,
                    gop.bpp,
                );
            }
        }
        if let Some(pci) = pci_fb::probe() {
            return Self::from_raw(pci.addr, pci.pitch, pci.width, pci.height, pci.bpp);
        }
        None
    }

    pub fn from_raw(
        addr: usize,
        pitch: u32,
        width: u32,
        height: u32,
        bpp: u8,
    ) -> Option<Self> {
        if addr == 0 || width == 0 || height == 0 {
            return None;
        }
        if bpp != 24 && bpp != 32 {
            return None;
        }
        Some(Self {
            addr,
            pitch,
            width,
            height,
            bpp,
        })
    }

    pub fn from_multiboot(info_addr: u32) -> Option<Self> {
        if info_addr == 0 {
            return None;
        }
        let info = unsafe { &*(info_addr as *const MultibootInfoFb) };
        if (info.flags & (1 << 12)) == 0 {
            return None;
        }
        if info.framebuffer_addr == 0 || info.framebuffer_width == 0 || info.framebuffer_height == 0
        {
            return None;
        }

        let bpp = if info.framebuffer_bpp == 24 || info.framebuffer_bpp == 32 {
            info.framebuffer_bpp
        } else {
            32
        };
        let pitch = if info.framebuffer_pitch != 0 {
            info.framebuffer_pitch
        } else {
            info.framebuffer_width * (bpp as u32 / 8)
        };

        Self::from_raw(
            info.framebuffer_addr as usize,
            pitch,
            info.framebuffer_width,
            info.framebuffer_height,
            bpp,
        )
    }

    pub fn fill_rect(&self, x: u32, y: u32, w: u32, h: u32, color: u32) {
        let w = w.min(self.width.saturating_sub(x));
        let h = h.min(self.height.saturating_sub(y));
        for row in y..y + h {
            for col in x..x + w {
                self.put_pixel(col, row, color);
            }
        }
    }

    pub fn clear(&self, color: u32) {
        self.fill_rect(0, 0, self.width, self.height, color);
    }

    pub fn write_str(&self, mut x: u32, mut y: u32, text: &str, color: u32, scale: u32) {
        for ch in text.bytes() {
            if ch == b'\n' {
                y += 8 * scale + 2;
                x = 8;
                continue;
            }
            if ch < 32 || ch > 127 {
                continue;
            }
            self.draw_char(x, y, ch, color, scale);
            x += 8 * scale + 1;
            if x + 8 * scale >= self.width {
                x = 8;
                y += 8 * scale + 2;
            }
        }
    }

    fn draw_char(&self, x: u32, y: u32, ch: u8, color: u32, scale: u32) {
        let glyph = &FONT8X8[(ch - 32) as usize];
        for row in 0..8u32 {
            let bits = glyph[row as usize];
            for col in 0..8u32 {
                if (bits >> col) & 1 == 1 {
                    for dy in 0..scale {
                        for dx in 0..scale {
                            self.put_pixel(
                                x + col * scale + dx,
                                y + row * scale + dy,
                                color,
                            );
                        }
                    }
                }
            }
        }
    }

    fn put_pixel(&self, x: u32, y: u32, color: u32) {
        if x >= self.width || y >= self.height {
            return;
        }
        let off = self.addr + (y * self.pitch + x * (self.bpp as u32 / 8)) as usize;
        if self.bpp == 32 {
            unsafe {
                (off as *mut u32).write_volatile(color);
            }
        } else {
            // 24bpp BGR
            let b = (color & 0xFF) as u8;
            let g = ((color >> 8) & 0xFF) as u8;
            let r = ((color >> 16) & 0xFF) as u8;
            unsafe {
                (off as *mut u8).write_volatile(b);
                (off as *mut u8).add(1).write_volatile(g);
                (off as *mut u8).add(2).write_volatile(r);
            }
        }
    }
}